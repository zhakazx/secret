    private void btnHitungActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnHitungActionPerformed
        int diskon = 0;
        int totalBayar = 0;
        
        int jumlahTiket = Integer.parseInt(txtJumlahTiket.getText());
        int hargaTiket = Integer.parseInt(txtHargaTiket.getText());
        int subTotal = (jumlahTiket * hargaTiket);
        
        if(txtMember.isSelected()) {
            diskon = (10 * hargaTiket) / 100;
        }
        
        totalBayar = (subTotal - diskon);
        
        txtSubTotal.setText(String.valueOf(subTotal));
        txtDiskon.setText(String.valueOf(diskon));
        txtTotalBayar.setText(String.valueOf(totalBayar));
    }//GEN-LAST:event_btnHitungActionPerformed

    private void cboTujuanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cboTujuanActionPerformed
        String tujuan = (String)cboTujuan.getSelectedItem();
        int harga = 0;
        if(tujuan == "Jakarta") {
            harga = 950000;
        } else if (tujuan == "Bali") {
            harga = 1200000;
        } else if (tujuan == "Surabaya") {
            harga = 1000000;
        } else {
            harga = 950000;
        }
        
        txtHargaTiket.setText(String.valueOf(harga));
    }//GEN-LAST:event_cboTujuanActionPerformed

    private void btnSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimpanActionPerformed
        String nama = txtNama.getText();
        String tujuan = (String)cboTujuan.getSelectedItem();
        String jml_tiket = txtJumlahTiket.getText();
        String isMember = txtMember.getText();
        String harga = txtHargaTiket.getText();
        String sub_ttl = txtSubTotal.getText();
        String diskon = txtDiskon.getText();
        String ttl_bayar = txtTotalBayar.getText();
        try {
            Statement statement = (Statement)Koneksi.getConnection().createStatement();
            String sql = 
                "INSERT INTO transaksi (nama,tujuan,jumlah_tiket,isMember,harga_tiket,sub_total,diskon,total_bayar) VALUES"
                + "('"+nama+"','"+tujuan+"','"+jml_tiket+"','"+isMember+"','"+harga+"','"+sub_ttl+"','"+diskon+"','"+ttl_bayar+"')";                   
            statement.executeUpdate(sql);
            load_table();
            clears();
        } catch(Exception e) {
            System.out.println("Error : " + e);
        }
    }//GEN-LAST:event_btnSimpanActionPerformed

