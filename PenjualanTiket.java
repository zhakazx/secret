    private void load_table() {
        DefaultTableModel model = new DefaultTableModel();
        model.addColumn("Nama");
        model.addColumn("Tujuan");
        model.addColumn("Jumlah Tiiket");
        model.addColumn("Member");
        model.addColumn("Harga Tiket");
        model.addColumn("Sub Total");
        model.addColumn("Diskon");
        model.addColumn("Total Bayar");
        
        try {
            String sql = "SELECT * FROM transaksi";
            Statement statement = (Statement) Koneksi.getConnection().createStatement();
            ResultSet res= statement.executeQuery(sql);
            while(res.next()){
                model.addRow(new Object[]{
                    res.getString("nama"),
                    res.getString("tujuan"),
                    res.getString("jumlah_tiket"),
                    res.getString("isMember"),
                    res.getString("harga_tiket"),
                    res.getString("sub_total"),
                    res.getString("diskon"),
                    res.getString("total_bayar")
                });
            }
            tableTiket.setModel(model);
        } catch (Exception e) {
        }
    }
